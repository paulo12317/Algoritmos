let variavel1 = 50
let variavel2 = 35

//Igual: ===
//verifica se dois valores sao iguais
console.log(variavel1 === variavel2) //false
console.log(variavel1 === 50) //true
console.log(variavel1 === (2*25)) //true

//diferente: !==
//verifica se dois valores sao diferentes
console.log(variavel1 !== variavel2)

//maior <
//verifica se o valor da esquerda é maior que o valor da direita 

console.log(variavel1 > variavel2) //true
console.log(variavel1 > 50) //false

//maior e maior igual: >=
//verifica se o valor da esquerda é maior ou igual que o da direita
console.log(variavel1 >= variavel2) //trues
console.log(50 >= 50) //trues
console.log(35 >=  50) //false

//menor: <
//verifica se o valor da esquerda é menor que o da direita
console.log(variavel1 < variavel2) //false
console.log(24 < 50) //true

//menor ou igual <=
//verifica se o valor da esquerda é menor ou igual que o da direita 
console.log(variavel1 <= variavel2) //false
console.log(24 <= 50) //true
